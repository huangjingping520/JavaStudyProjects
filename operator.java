/**
 * operator
 */
public class operator {

    public static void main(String[] args) {
        // 二元运算符
        int a = 10;
        int b = 20;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / (double)b);
    }
}